package com.javaex.ex04;

public class Ex05 {

	public static void main(String[] args) {
		//변수
		int i;
		int result=0;
		
		//for문
		for (i=1; i<=10; i++) {
			
			//조건:1부터10까지의 정수의 합
			
			result = result+i;
			
			System.out.println(i+" 까지의 "+ " 합은 " + result);
			
		} System.out.println((i-10)+"부터 10까지의 정수의 합은 "+ result + " 입니다.");
		
		
		
		
		
		
		
	}

}
